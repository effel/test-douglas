 'use strict';


// Function fr data array slicing
function sliceCurrentArr(startPos, endPos, arr) {
   let sliced = arr.slice(startPos, endPos);
   return sliced;
};

// Creating item html template
function createItemTemplate(itemObj) {
    let itemTypeClass = "";

    switch (itemObj.label) {
      case "Fruit":
        itemTypeClass = "yellow";
        break;
      case "Vegetable":
        itemTypeClass = "green";
        break;
      case "Meat":
        itemTypeClass = "red";
        break;
       default:
        itemTypeClass = "";
    };

    const itemTempl = '<div class="item">' +
          '<figure><img src="' + itemObj.img + '" alt=""></figure>' +
          '<div class="item-content">' +
            '<h2>' + itemObj.title + '<span class="badge ' + itemTypeClass + '" >' + itemObj.label + '</span></h2>' +
            '<article>' +
              itemObj.textContent +
            '</article>' +
            '<a href="" class="more-info">' +
              '<i class="fa fa-angle-right ' + itemTypeClass + '" ></i>' + 
              '<span>More info</span>' + 
            '</a>' +
          '</div>' +
        '</div>';
  return itemTempl; 
};

//creating list of items for current state
function createItemsListView(startPosition, endPosition, currentData) {
    const itemsParentContainer = document.querySelector('.item-wrapper');   
    const currentItemData = sliceCurrentArr(startPosition, endPosition, currentData);     
    let itemsTeml = '';        
    currentItemData.forEach(function(element) {
        itemsTeml += createItemTemplate(element);
    });
    return itemsParentContainer.innerHTML = itemsTeml;
};

// Function with actions for window on load event
function init() {
    
  const pagesCount = 6; 
  
  // Function for showing nav menu links with click on it actions
  function showClickNavMenu() {
    let linkTempl = ""; 
    const navMenuElement = document.querySelector('nav');
    const navMenuElementLinksArr = navMenuElement.children;
    const itemsCount = Math.floor(itemsData.length/pagesCount); 

    for (let i = 0; i < pagesCount; i++ ) {
        let itemNum = i + 1;
        linkTempl += '<a href="" data-link-order="' + i + '" > Menu Item' + itemNum + '</a>';
    };
    navMenuElement.innerHTML = linkTempl;

    createItemsListView(0, itemsCount, itemsData);  
    navMenuElementLinksArr[0].classList.add("active");  

    navMenuElement.addEventListener("click", function(e) {
        e.preventDefault();

        if (e.target.tagName.toLowerCase() === 'a')
        {
            const linkElem = e.target; 

            for (let i = 0; i < navMenuElementLinksArr.length; i++ ) {
               if (navMenuElementLinksArr[i] === linkElem) {
                  linkElem.classList.add("active");
               } else {
                  navMenuElementLinksArr[i].classList.remove("active");
               }
            };            


            const startItemsPosition = +(linkElem.getAttribute('data-link-order')); 
            const endItemsPosition = startItemsPosition + itemsCount;
            createItemsListView(startItemsPosition, endItemsPosition, itemsData);
        };

    });    
  };

  showClickNavMenu();

  // show/hide mobile menu on trigger click;
  document.querySelector('.mobile-menu-trigger').addEventListener("click", function(e) {
      this.classList.toggle("active");
      if (this.classList.contains("active")) {
         document.querySelector('nav').classList.add("visible");
      } else {
         document.querySelector('nav').classList.remove("visible");
      };
  }); 


    document.querySelector('.filter-block').addEventListener("click", function(e) {

        if (e.target.tagName.toLowerCase() === 'input')
        {
            const checkElem = e.target; 
            const currentData = itemsData;
            const elemType = checkElem.getAttribute('data-type');

            if (checkElem.checked) {

               var filtered = currentData.filter(function(elem){

                   return elem.label === elemType;
               });

               console.log(filtered)

            }
           
        };

    }); 

};

